#include <stdio.h>
/**
*add - add two numbers
*@a: first number
*@b: second number
*Return: the sum
*/
int add(int a, int b){
return a + b;
}
/**
*sub - substrat a number
*@a: first number
*@b: second number
*Return: the diff
*/
int sub(int a, int b){
return a - b;
}
/**
*mul - multiply numbers
*@a: first number
*@b: second number
*Return: the product
*/
int mul(int a, int b){
return a * b;
}
/**
*div - divides two numbers
*@a: first number
*@b: second number
*Return: the quotient
*/
int div(int a, int b){
return a / b;
}
/**
*mod - modulo
*@a: first number
*@b: second number
*Return: the rest of division
*/
int mod(int a, int b){
return a % b;
}
