#include "3-calc.h"
/**
*op_add - addition
*@a: first number
*@b: second number
*Return: the sum
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
*op_sub - substraction
*@a: first number
*@b: second number
*Return: the difference
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
*op_mul - multiplication
*@a: first number
*@b: second number
*Return: the product
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
*op_div - Division
*@a: first number
*@b: second number
*Return: the quotient
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
*op_mod - modulo
*@a: first number
*@b: second number
*Return: the remainder
*/
int op_mod(int a, int b)
{
return (a % b);
}
